package proyectoedd;

/**
 * Clase que permite crear un contenedor para los datos extraídos del archivo de texto.
 * Contiene un array para almacenar los usuarios y otro (una matriz) para las relaciones. 
 * @see Interfaz
 * @author José Viloria
 */
public class Ginfo {
    public String[] usuarios;
    public String[][] relaciones;

    /**
     * Constructor de la clase Ginfo.
     */
    public Ginfo() {
        this.usuarios = usuarios;
        this.relaciones = relaciones;
    }
}
